def uppercase_string(input_string):
    """Принимает строку и возвращает ее с использованием только заглавных букв"""
    return input_string.upper()

def capitalize_first_letters(input_string):
    """Принимает строку и возвращает ее с первыми буквами каждого слова в верхнем регистре."""
    return input_string.title()
